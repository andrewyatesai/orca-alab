//! Trusted data-flow provenance framework (Phase 0; issue #8000, parent #7877).
//!
//! This crate provides the type-level machinery that lets the Rust compiler
//! refuse to let PTY-origin data reach a host-privileged sink without passing
//! through a named `authorize_*` ceremony that consumes a zero-sized capability
//! token.
//!
//! The Terminal-class RCE (#7875) was fixed with one such ceremony
//! (`ConductorActivationToken`). This framework generalizes that pattern.
//!
//! # Overview
//!
//! * [`Origin`] is a sealed trait implemented by 6 marker types forming the
//!   lattice described in `designs/2026-04-19-provenance-framework.md` ┬¦3:
//!   [`Host`], [`ConfigFile`], [`User`], [`Ai`], [`NetworkUntrusted`], [`Pty`].
//! * [`OriginTag`] is the runtime-shaped mirror of `Origin` (a plain `#[repr(u8)]`
//!   enum) used by [`DynProvenance`] and per-row grid metadata.
//! * [`Provenance<T, O>`] wraps a value with a compile-time origin marker.
//!   It is `#[repr(transparent)]`: `size_of::<Provenance<T, O>>() == size_of::<T>()`.
//! * [`DynProvenance<T>`] carries a runtime [`OriginTag`] (used at storage /
//!   FFI boundaries where a static type parameter is not available).
//! * [`JoinWith<Rhs>`] encodes the lattice join table at the type level. The
//!   36 impls are generated by `build.rs` from the matrix in ┬¦3.1.
//! * [`SelfSameOrigin`] and [`OriginsCompatible`] let sinks express the bound
//!   "require exactly origin X" vs. "require at least X".
//! * [`authorize_pty_to_host`] and [`authorize_network_to_host`] are the two
//!   edges that lift an untrusted origin to [`Host`]. Each consumes a
//!   zero-sized capability token so the lift is auditable (`grep -rn 'fn authorize_'`).
//! * [`Top`] is a synthetic "mixed untrusted ŌĆö cannot be lifted" element that
//!   appears only in [`DynProvenance`] combinations (never as a static type
//!   parameter). Attempting to lift a `Top`-tagged value returns [`UnliftableTop`].
//!
//! Phase 0 intentionally does **not** wire any of this into production code.
//! That is the job of the Phase 1 issue (#8005, `ActionSink` plumbing) and the
//! Phase 2 issues (#8001 capability wiring, #8002 checkpoint v4 migration).

